SELECT empcode,e_name"Name",department_name AS "Department", desig_name "Designation",TO_CHAR (JOIN_DATE, 'DD-MON-yyyy') "Join Date",
salarygrade"Grade",DEPOT "Location",dp_code"Station",BLD_GROUP FROM emp WHERE emp_status = 'A' and depot IN ('Head Office') 
AND department_name = 'Information Technology' ORDER BY SALARYGRADE, join_date;

