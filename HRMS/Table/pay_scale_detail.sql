CREATE OR REPLACE TRIGGER HRMS.t_pay_scale_detail_pk
    BEFORE INSERT OR UPDATE
    ON HRMS.PAY_SCALE_DETAIL
    FOR EACH ROW
BEGIN
    IF :new.DETAIL_ID IS NULL
    THEN
        SELECT NVL (MAX (DETAIL_ID), 0) + 1
          INTO :new.DETAIL_ID
          FROM pay_scale_detail;
    END IF;
END t_pay_scale_detail_pk;
