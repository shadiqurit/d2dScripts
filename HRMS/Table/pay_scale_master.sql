CREATE OR REPLACE TRIGGER HRMS.t_pay_scale_master_pk
    BEFORE INSERT OR UPDATE
    ON HRMS.PAY_SCALE_MASTER
    FOR EACH ROW
BEGIN
    IF :new.scale_id IS NULL
    THEN
        SELECT NVL (MAX (scale_id), 0) + 1
          INTO :new.scale_id
          FROM pay_scale_master;
    END IF;
END t_pay_scale_master_pk;
