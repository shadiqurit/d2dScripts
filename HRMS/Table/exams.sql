DROP TABLE HRMS.EXAMS CASCADE CONSTRAINTS;

CREATE TABLE HRMS.EXAMS
(
  ID        NUMBER,
  NAME      VARCHAR2(100 BYTE),
  ENT_BY    NUMBER,
  ENT_DATE  DATE,
  UPD_BY    NUMBER,
  UPD_DATE  DATE,
  TYP       VARCHAR2(10 BYTE)
)
TABLESPACE HRMS
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE;


CREATE UNIQUE INDEX HRMS.EXAMS_PK ON HRMS.EXAMS
(ID)
LOGGING
