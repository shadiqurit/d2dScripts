  CONSTRAINT HR_ADV_LEDGER_CK_AMOUNT
  CHECK (
            NVL(DR_AMOUNT,0) >= 0
            AND NVL(CR_AMOUNT,0) >= 0
            AND (
                NVL(DR_AMOUNT,0) > 0
                OR NVL(CR_AMOUNT,0) > 0
            )
            AND NOT (
                NVL(DR_AMOUNT,0) > 0
                AND NVL(CR_AMOUNT,0) > 0
            )
        )
