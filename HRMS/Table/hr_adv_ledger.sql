            'MANUAL_RECOVERY',
            'CASH_RETURN',
            'WAIVER',
            'ADJUSTMENT_DR',
            'ADJUSTMENT_CR',
            'REVERSAL'
        ))
  ENABLE VALIDATE
,  PRIMARY KEY
  (LEDGER_ID)
  USING INDEX
    TABLESPACE HRMS
    PCTFREE    10
    INITRANS   2
